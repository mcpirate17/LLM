"""Nano-corpus v0 train + eval harness (cheat-check).

Reads the corpus generated by ``research.tools.nano_corpus_v0``, trains a
fresh-init copy of ``ref_retrieval_augmented_ab5cf5ae`` directly on it (no
random-token warmup), and evaluates 10 ``the {noun} was ___`` prompts at
6 checkpoints.

Grading: top-1 next-token after the prompt is one of the 10 adjectives in
the corpus.  Test prompts are intentionally inside the training set
(cheat-check) — this run validates the harness, not the held-out
generalization claim.

Single seed.  No DB writes.  No scoring/gate change.
"""

from __future__ import annotations

import argparse
import json
import logging
import sqlite3
import time
from pathlib import Path
from typing import Any

import torch
import torch.nn as nn

from research.eval._probe_utils import next_token_train_step
from research.eval.utils import _get_tiktoken_encoder, make_adamw
from research.scientist.notebook.graph_artifacts import resolve_graph_json_value
from research.scientist.native_runner import compile_model_native_first as compile_model
from research.synthesis.serializer import graph_from_json
from research.tools.nano_corpus_v0 import ADJECTIVES

logger = logging.getLogger(__name__)

REF_RESULT_ID = "ref_retrieval_augmented_ab5cf5ae"
DEFAULT_CHECKPOINTS: tuple[int, ...] = (0, 50, 100, 200, 500, 1000)
DEFAULT_LR = 1e-3
DEFAULT_BATCH = 32
DEFAULT_SEED = 0
TIKTOKEN_ENCODING = "cl100k_base"  # matches compile_model's vocab=100277
PAD_ID = 0


def _load_graph_json(db: Path, result_id: str) -> str:
    conn = sqlite3.connect(f"file:{db}?mode=ro", uri=True)
    try:
        row = conn.execute(
            "SELECT graph_json FROM program_results_compat WHERE result_id = ? LIMIT 1",
            (result_id,),
        ).fetchone()
        if not row or not row[0]:
            raise RuntimeError(f"no graph_json for {result_id!r}")
        return resolve_graph_json_value(conn, db, row[0])
    finally:
        conn.close()


def _read_corpus(path: Path) -> list[str]:
    sentences: list[str] = []
    for line in path.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        sentences.append(line)
    return sentences


def _tokenize_words(words: list[str], enc: Any) -> list[int]:
    """Each word becomes one token via leading-space convention.  Matches v3
    probe's tokenization choice."""
    out: list[int] = []
    for w in words:
        ids = enc.encode(" " + w, allowed_special=set())
        if len(ids) != 1:
            raise ValueError(
                f"word {w!r} did not single-token under {TIKTOKEN_ENCODING}"
            )
        out.append(int(ids[0]))
    return out


def _build_train_tensor(
    corpus: list[str], enc: Any, device: torch.device
) -> torch.Tensor:
    rows: list[list[int]] = []
    max_len = 0
    for sent in corpus:
        ids = _tokenize_words(sent.split(), enc)
        rows.append(ids)
        max_len = max(max_len, len(ids))
    out = torch.full((len(rows), max_len), PAD_ID, dtype=torch.long, device=device)
    for i, row in enumerate(rows):
        out[i, : len(row)] = torch.tensor(row, dtype=torch.long, device=device)
    return out


def _build_prompt_tensors(
    prompts: list[str], enc: Any, device: torch.device
) -> tuple[torch.Tensor, list[int]]:
    """Return (padded_tensor, last_real_position_per_row)."""
    rows: list[list[int]] = []
    last_pos: list[int] = []
    max_len = 0
    for sent in prompts:
        ids = _tokenize_words(sent.split(), enc)
        rows.append(ids)
        last_pos.append(len(ids) - 1)
        max_len = max(max_len, len(ids))
    out = torch.full((len(rows), max_len), PAD_ID, dtype=torch.long, device=device)
    for i, row in enumerate(rows):
        out[i, : len(row)] = torch.tensor(row, dtype=torch.long, device=device)
    return out, last_pos


def _train_steps(
    model: nn.Module,
    train_ids: torch.Tensor,
    *,
    n_steps: int,
    batch_size: int,
    lr: float,
    seed: int,
) -> None:
    """Train for ``n_steps`` mini-batch updates.  Resamples each batch
    uniformly from training rows (which is fine for a 280-sentence corpus)."""
    if n_steps <= 0:
        return
    model.train()
    opt = make_adamw(model.parameters(), lr=lr)
    rng = torch.Generator(device=train_ids.device)
    rng.manual_seed(int(seed))
    n = train_ids.shape[0]
    for _ in range(int(n_steps)):
        idx = torch.randint(
            0, n, (int(batch_size),), generator=rng, device=train_ids.device
        )
        batch = train_ids.index_select(0, idx)
        if not next_token_train_step(model, batch, opt, pad_id=PAD_ID):
            logger.warning("non-finite loss at training step; aborting train segment")
            return


def _aggregate(per_prompt: list[dict[str, Any]]) -> dict[str, Any]:
    n = len(per_prompt)
    if n == 0:
        return {
            "n_prompts": 0,
            "top1_adjective_acc": 0.0,
            "top1_corpus_vocab_rate": 0.0,
            "top5_adjective_count_mean": 0.0,
        }
    return {
        "n_prompts": n,
        "top1_adjective_acc": round(
            sum(p["top1_is_adjective"] for p in per_prompt) / n, 4
        ),
        "top1_corpus_vocab_rate": round(
            sum(p["top1_in_corpus_vocab"] for p in per_prompt) / n, 4
        ),
        "top5_adjective_count_mean": round(
            sum(p["top5_adjective_count"] for p in per_prompt) / n, 3
        ),
    }


@torch.no_grad()
def _eval_prompts(
    model: nn.Module,
    prompt_ids: torch.Tensor,
    last_pos: list[int],
    adj_token_ids: set[int],
    corpus_token_ids: set[int],
    enc: Any,
    prompt_texts: list[str],
    held_out_nouns: frozenset[str] = frozenset(),
) -> dict[str, Any]:
    model.eval()
    logits = model(prompt_ids)
    per_prompt: list[dict[str, Any]] = []
    for i, lp in enumerate(last_pos):
        row_logits = logits[i, lp, :].float()
        top5 = torch.topk(row_logits, k=5)
        top5_ids = [int(t) for t in top5.indices.tolist()]
        top1 = top5_ids[0]
        prompt_noun = (
            prompt_texts[i].split()[1] if len(prompt_texts[i].split()) >= 2 else ""
        )
        per_prompt.append(
            {
                "prompt": prompt_texts[i],
                "noun": prompt_noun,
                "is_held_out": prompt_noun in held_out_nouns,
                "top1_token_id": top1,
                "top1_token": enc.decode([top1]).strip(),
                "top1_is_adjective": top1 in adj_token_ids,
                "top1_in_corpus_vocab": top1 in corpus_token_ids,
                "top5_adjective_count": sum(1 for t in top5_ids if t in adj_token_ids),
                "top5_tokens": [enc.decode([t]).strip() for t in top5_ids],
            }
        )
    out = {"per_prompt": per_prompt, **_aggregate(per_prompt)}
    if held_out_nouns:
        held = [p for p in per_prompt if p["is_held_out"]]
        in_dist = [p for p in per_prompt if not p["is_held_out"]]
        out["held_out"] = _aggregate(held)
        out["in_dist"] = _aggregate(in_dist)
    return out


def _print_summary(report: dict[str, Any]) -> None:
    has_split = "held_out" in report["checkpoints"][0]
    print()
    if has_split:
        print("## Per-checkpoint accuracy (held-out vs in-dist)")
        print(
            "| step | overall adj acc | held adj acc | in-dist adj acc | top5 adj mean |"
        )
        print("|---:|---:|---:|---:|---:|")
        for cp in report["checkpoints"]:
            print(
                f"| {cp['step']} | {cp['top1_adjective_acc']:.2f} | "
                f"{cp['held_out']['top1_adjective_acc']:.2f} | "
                f"{cp['in_dist']['top1_adjective_acc']:.2f} | "
                f"{cp['top5_adjective_count_mean']:.2f} |"
            )
    else:
        print("## Per-checkpoint accuracy")
        print("| step | top1 adj acc | top1 in-corpus rate | top5 adj count (mean) |")
        print("|---:|---:|---:|---:|")
        for cp in report["checkpoints"]:
            print(
                f"| {cp['step']} | {cp['top1_adjective_acc']:.2f} | "
                f"{cp['top1_corpus_vocab_rate']:.2f} | "
                f"{cp['top5_adjective_count_mean']:.2f} |"
            )
    print()
    final = report["checkpoints"][-1]
    print(f"## Per-prompt breakdown at final checkpoint (step={final['step']})")

    def label(p):
        return "(HELD)" if p.get("is_held_out") else ""

    print("| prompt | top-1 | adj? | top-5 |")
    print("|---|---|---:|---|")
    for p in final["per_prompt"]:
        prefix = label(p)
        print(
            f"| `{p['prompt']}` {prefix} | `{p['top1_token']}` | "
            f"{'✓' if p['top1_is_adjective'] else '·'} | "
            f"{', '.join(p['top5_tokens'])} |"
        )


def _parse_args() -> argparse.Namespace:
    ap = argparse.ArgumentParser()
    ap.add_argument("--db", type=Path, default=Path("research/runs.db"))
    ap.add_argument(
        "--corpus", type=Path, default=Path("research/reports/nano_corpus_v0.txt")
    )
    ap.add_argument(
        "--out",
        type=Path,
        default=Path("research/reports/nano_corpus_v0_train_eval.json"),
    )
    ap.add_argument("--device", default="cuda" if torch.cuda.is_available() else "cpu")
    ap.add_argument("--seed", type=int, default=DEFAULT_SEED)
    ap.add_argument("--lr", type=float, default=DEFAULT_LR)
    ap.add_argument("--batch-size", type=int, default=DEFAULT_BATCH)
    ap.add_argument(
        "--checkpoints", nargs="+", type=int, default=list(DEFAULT_CHECKPOINTS)
    )
    ap.add_argument("--ref-id", default=REF_RESULT_ID)
    ap.add_argument(
        "--held-out-nouns",
        nargs="*",
        default=(),
        help="Nouns flagged as held-out in eval (split metrics by held vs in-dist). "
        "Must match the --hold-out value used when generating the corpus.",
    )
    return ap.parse_args()


def _prepare(
    args: argparse.Namespace,
) -> tuple[
    nn.Module, torch.Tensor, torch.Tensor, list[int], list[str], set[int], set[int], Any
]:
    device = torch.device(args.device)
    enc = _get_tiktoken_encoder(TIKTOKEN_ENCODING)
    sentences = _read_corpus(args.corpus)
    train_sentences = [s for s in sentences if not s.endswith(" was")]
    prompt_sentences = [s for s in sentences if s.endswith(" was")]
    logger.info(
        "read %d sentences from %s (%d train + %d test prompts)",
        len(sentences),
        args.corpus,
        len(train_sentences),
        len(prompt_sentences),
    )
    if len(prompt_sentences) != 10:
        raise RuntimeError(f"expected 10 test prompts, got {len(prompt_sentences)}")
    train_ids = _build_train_tensor(train_sentences, enc, device)
    prompt_ids, last_pos = _build_prompt_tensors(prompt_sentences, enc, device)
    # Derive adjective set from the actual corpus rather than ADJECTIVES constant.
    # This makes the eval honest under vocab-size sweeps: only adjectives the
    # model actually saw in training count as "right slot type."
    corpus_adjectives = set()
    for sent in train_sentences:
        toks = sent.split()
        for i, t in enumerate(toks):
            if t == "was" and i + 1 < len(toks):
                corpus_adjectives.add(toks[i + 1])
            if (
                t == "the"
                and i + 2 < len(toks)
                and toks[i + 1] in {a for a in ADJECTIVES}
            ):
                # bucket-C "the {adj} {noun} ..." case
                corpus_adjectives.add(toks[i + 1])
    if not corpus_adjectives:
        corpus_adjectives = set(ADJECTIVES)
    adj_token_ids = {
        int(enc.encode(" " + a, allowed_special=set())[0]) for a in corpus_adjectives
    }
    all_words = set(w for sent in train_sentences for w in sent.split())
    corpus_token_ids = {
        int(enc.encode(" " + w, allowed_special=set())[0]) for w in all_words
    }
    logger.info(
        "corpus adjective vocab: %d words (%s)",
        len(corpus_adjectives),
        sorted(corpus_adjectives),
    )
    logger.info("loading graph_json for %s", args.ref_id)
    graph = graph_from_json(_load_graph_json(args.db, args.ref_id))
    torch.manual_seed(int(args.seed))
    model = compile_model([graph]).to(device)
    logger.info(
        "model param count: %d (vocab=%d)",
        sum(p.numel() for p in model.parameters()),
        int(getattr(model, "vocab_size", 0)),
    )
    return (
        model,
        train_ids,
        prompt_ids,
        last_pos,
        prompt_sentences,
        adj_token_ids,
        corpus_token_ids,
        enc,
    )


def _run_checkpoints(
    model: nn.Module,
    train_ids: torch.Tensor,
    prompt_ids: torch.Tensor,
    last_pos: list[int],
    prompt_sentences: list[str],
    adj_token_ids: set[int],
    corpus_token_ids: set[int],
    enc: Any,
    *,
    checkpoints: list[int],
    seed: int,
    lr: float,
    batch_size: int,
    held_out_nouns: frozenset[str] = frozenset(),
) -> list[dict[str, Any]]:
    out: list[dict[str, Any]] = []
    prev_step = 0
    for cp_step in checkpoints:
        delta = max(0, int(cp_step) - prev_step)
        train_wall = 0.0
        if delta:
            t0 = time.perf_counter()
            _train_steps(
                model,
                train_ids,
                n_steps=delta,
                batch_size=batch_size,
                lr=lr,
                seed=seed + cp_step,
            )
            train_wall = round(time.perf_counter() - t0, 2)
        eval_data = _eval_prompts(
            model,
            prompt_ids,
            last_pos,
            adj_token_ids=adj_token_ids,
            corpus_token_ids=corpus_token_ids,
            enc=enc,
            prompt_texts=prompt_sentences,
            held_out_nouns=held_out_nouns,
        )
        out.append({"step": int(cp_step), "train_wall_s": train_wall, **eval_data})
        logger.info(
            "step=%d top1_adj_acc=%.2f corpus_rate=%.2f top5_adj_mean=%.2f wall=%.1fs",
            cp_step,
            eval_data["top1_adjective_acc"],
            eval_data["top1_corpus_vocab_rate"],
            eval_data["top5_adjective_count_mean"],
            train_wall,
        )
        prev_step = int(cp_step)
    return out


def main() -> int:
    args = _parse_args()
    logging.basicConfig(level=logging.INFO, format="%(levelname)s %(message)s")
    (
        model,
        train_ids,
        prompt_ids,
        last_pos,
        prompt_sentences,
        adj_token_ids,
        corpus_token_ids,
        enc,
    ) = _prepare(args)
    held_out_nouns = frozenset(args.held_out_nouns)
    t_start = time.perf_counter()
    checkpoints = _run_checkpoints(
        model,
        train_ids,
        prompt_ids,
        last_pos,
        prompt_sentences,
        adj_token_ids,
        corpus_token_ids,
        enc,
        checkpoints=list(args.checkpoints),
        seed=int(args.seed),
        lr=float(args.lr),
        batch_size=int(args.batch_size),
        held_out_nouns=held_out_nouns,
    )
    total_wall = round(time.perf_counter() - t_start, 2)
    report = {
        "ref_id": args.ref_id,
        "seed": args.seed,
        "lr": args.lr,
        "batch_size": args.batch_size,
        "n_train_sentences": int(train_ids.shape[0]),
        "n_prompts": int(prompt_ids.shape[0]),
        "held_out_nouns": sorted(held_out_nouns),
        "checkpoints": checkpoints,
        "total_wall_s": total_wall,
    }
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(json.dumps(report, indent=2))
    logger.info("wrote %s (total wall %.1fs)", args.out, total_wall)
    _print_summary(report)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
