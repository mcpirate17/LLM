"""
Morphological Box for Architecture Exploration

Defines the dimensions of variation for novel transformer architectures.
Each dimension has discrete options. A "roll" picks one option per dimension
to create a complete architecture specification.

Based on Zwicky's morphological analysis + TRIZ inventive principles.
"""

