DIMENSION_NAMES = tuple(d.name for d in DIMENSIONS)

# Pre-built lookup dicts for O(1) dimension/option access
_DIMENSION_MAP: Dict[str, Dimension] = {d.name: d for d in DIMENSIONS}
_OPTION_MAP: Dict[str, Dict[str, Option]] = {
    d.name: {o.name: o for o in d.options} for d in DIMENSIONS
}


# ── Architecture Specification ─────────────────────────────────────────

@dataclass
class ArchSpec:
    """A complete architecture specification from one morphological roll."""
    choices: Dict[str, str]  # dimension_name -> option_name
    seed: int
    generation: int = 0  # which generation/batch this came from
    parent_id: Optional[str] = None  # if mutated from another spec

    @property
    def id(self) -> str:
        """Deterministic hash ID from choices."""
        key = json.dumps(self.choices, sort_keys=True)
        return hashlib.sha256(key.encode()).hexdigest()[:12]

    @property
    def short_name(self) -> str:
        """Human-readable short name."""
        parts = []
        for dim_name in DIMENSION_NAMES:
            opt = self.choices.get(dim_name, "?")
            # Take first letter of each word
            abbrev = "".join(w[0].upper() for w in opt.split("_"))
            parts.append(abbrev)
        return "-".join(parts)

    def get_option(self, dimension: str) -> Option:
        """Get the full Option object for a dimension."""
        opt_name = self.choices[dimension]
        dim_opts = _OPTION_MAP.get(dimension)
        if dim_opts:
            opt = dim_opts.get(opt_name)
            if opt is not None:
                return opt
        raise KeyError(f"No option '{opt_name}' in dimension '{dimension}'")

    def all_tags(self) -> set:
        """Collect all tags from all chosen options."""
        tags = set()
        for dim_name in self.choices:
            try:
                opt = self.get_option(dim_name)
                tags.update(opt.tags)
            except KeyError:
                pass
        return tags

    def to_dict(self) -> Dict[str, Any]:
        return {
            "id": self.id,
            "short_name": self.short_name,
            "choices": self.choices,
            "seed": self.seed,
            "generation": self.generation,
            "parent_id": self.parent_id,
        }


# ── Constraint Filter ──────────────────────────────────────────────────

def _check_tag_incompatibilities(spec: ArchSpec) -> Optional[str]:
    """Check if any option's incompatible_with tags clash with other options' tags."""
    option_cache: Dict[str, Option] = {
        dim_name: spec.get_option(dim_name) for dim_name in spec.choices
    }
    tag_sources: Dict[str, List[Tuple[str, str]]] = {}
    for dim_name, opt in option_cache.items():
        for tag in opt.tags:
            tag_sources.setdefault(tag, []).append((dim_name, opt.name))

    for dim_name, opt_name in spec.choices.items():
        opt = option_cache[dim_name]
        if not opt.incompatible_with:
            continue
        for bad_tag in opt.incompatible_with:
            for other_dim, other_name in tag_sources.get(bad_tag, []):
                if other_dim == dim_name:
                    continue
                return (f"{dim_name}={opt_name} incompatible with "
                        f"{other_dim}={other_name} (tags: {{{bad_tag}}})")
    return None


def _check_structural_constraints(spec: ArchSpec) -> Optional[str]:
    """Hard constraints that prevent nonsensical combinations."""
    c = spec.choices

    # Binary/quantized tokens + hypernetwork weights = unstable gradients
    if c["token_representation"] in ("binary_hash", "residual_quantized"):
        if c["weight_storage"] == "hypernetwork":
            return "binary/quantized tokens + hypernetwork weights: gradient flow too fragile"

    # No normalization + complex/quaternion = likely to diverge
    if c["normalization"] == "no_norm":
        if c["token_representation"] in ("complex_valued", "quaternion"):
            return "no normalization + complex representations: will diverge"

    # Identity channel mixing + identity compute routing = literally nothing
    if c["channel_mixing"] == "identity_skip" and c["compute_routing"] == "uniform":
        if c["token_mixing"] == "conv_only":
            return "conv-only mixing + identity MLP + uniform routing: too minimal"

    # State space + graph attention = conflicting paradigms
    if c["token_mixing"] == "state_space" and c["topology"] == "dense_net":
        return "SSM + DenseNet: SSM is inherently sequential, DenseNet adds O(n^2) connections"

    # Feedback loop + early exit = contradictory
    if c["topology"] == "feedback_loop" and c["compute_routing"] == "early_exit":
        return "feedback loops + early exit: contradictory (can't exit if looping back)"

    return None


def is_valid_spec(spec: ArchSpec) -> Tuple[bool, Optional[str]]:
    """Check if an architecture spec passes all constraints."""
    reason = _check_tag_incompatibilities(spec)
    if reason:
        return False, reason

    reason = _check_structural_constraints(spec)
    if reason:
        return False, reason

    return True, None


# ── Dice Roller ────────────────────────────────────────────────────────

def roll(
    seed: Optional[int] = None,
    generation: int = 0,
    fixed: Optional[Dict[str, str]] = None,
    max_retries: int = 100,
) -> ArchSpec:
    """
    Roll the morphological dice to generate a random architecture spec.

    Args:
        seed: Random seed (None for random)
        generation: Generation number for tracking
        fixed: Dict of dimension_name -> option_name to fix (not randomized)
        max_retries: Max attempts to find a valid combination

    Returns:
        A valid ArchSpec

    Raises:
        RuntimeError if no valid combination found in max_retries
    """
    if seed is None:
        seed = random.randint(0, 2**32 - 1)

    rng = random.Random(seed)
    fixed = fixed or {}

    for attempt in range(max_retries):
        choices = {}
        for dim in DIMENSIONS:
            if dim.name in fixed:
                choices[dim.name] = fixed[dim.name]
            else:
                choices[dim.name] = dim.random_option(rng).name

        spec = ArchSpec(
            choices=choices,
            seed=seed + attempt,  # vary seed on retry
            generation=generation,
        )

        valid, reason = is_valid_spec(spec)
        if valid:
            return spec

    raise RuntimeError(f"No valid combination found in {max_retries} retries")


def mutate(
    spec: ArchSpec,
    n_mutations: int = 1,
    seed: Optional[int] = None,
    generation: int = 0,
    max_retries: int = 100,
) -> ArchSpec:
    """
    Mutate an existing spec by changing N random dimensions.

    Args:
        spec: The parent spec to mutate
        n_mutations: Number of dimensions to change
        seed: Random seed
        generation: Generation number
        max_retries: Max attempts to find valid mutation

    Returns:
        A new valid ArchSpec with parent_id set
    """
    if seed is None:
        seed = random.randint(0, 2**32 - 1)

    rng = random.Random(seed)
    n_mutations = min(n_mutations, len(DIMENSIONS))

    for attempt in range(max_retries):
        new_choices = dict(spec.choices)
        dims_to_mutate = rng.sample(DIMENSION_NAMES, n_mutations)

        for dim_name in dims_to_mutate:
            for dim in DIMENSIONS:
                if dim.name == dim_name:
                    # Pick a DIFFERENT option than current
                    current = new_choices[dim_name]
                    candidates = [o for o in dim.options if o.name != current]
                    if candidates:
                        new_choices[dim_name] = rng.choice(candidates).name
                    break

        new_spec = ArchSpec(
            choices=new_choices,
            seed=seed + attempt,
            generation=generation,
            parent_id=spec.id,
        )

        valid, reason = is_valid_spec(new_spec)
        if valid:
            return new_spec

    raise RuntimeError(f"No valid mutation found in {max_retries} retries")


def crossover(
    spec_a: ArchSpec,
    spec_b: ArchSpec,
    seed: Optional[int] = None,
    generation: int = 0,
    max_retries: int = 100,
) -> ArchSpec:
    """
    Crossover two specs: randomly pick each dimension from one parent.

    Returns:
        A new valid ArchSpec
    """
    if seed is None:
        seed = random.randint(0, 2**32 - 1)

    rng = random.Random(seed)

    for attempt in range(max_retries):
        choices = {}
        for dim_name in DIMENSION_NAMES:
            if rng.random() < 0.5:
                choices[dim_name] = spec_a.choices[dim_name]
            else:
                choices[dim_name] = spec_b.choices[dim_name]

        new_spec = ArchSpec(
            choices=choices,
            seed=seed + attempt,
            generation=generation,
            parent_id=f"{spec_a.id}x{spec_b.id}",
        )

        valid, reason = is_valid_spec(new_spec)
        if valid:
            return new_spec

    raise RuntimeError(f"No valid crossover found in {max_retries} retries")


def batch_roll(
    n: int,
    generation: int = 0,
    fixed: Optional[Dict[str, str]] = None,
    base_seed: Optional[int] = None,
) -> List[ArchSpec]:
    """Generate N unique valid architecture specs."""
    if base_seed is None:
        base_seed = random.randint(0, 2**32 - 1)

    specs = []
    seen_ids = set()
    seed = base_seed

    while len(specs) < n:
        spec = roll(seed=seed, generation=generation, fixed=fixed)
        if spec.id not in seen_ids:
            specs.append(spec)
            seen_ids.add(spec.id)
        seed += 1000  # jump ahead to avoid correlation

    return specs


# ── Pretty Print ───────────────────────────────────────────────────────

def describe_spec(spec: ArchSpec) -> str:
    """Pretty-print an architecture spec."""
    lines = [f"Architecture {spec.id} (seed={spec.seed}, gen={spec.generation})"]
    if spec.parent_id:
        lines.append(f"  Parent: {spec.parent_id}")
    lines.append(f"  Name: {spec.short_name}")
    lines.append("")

    for dim in DIMENSIONS:
        opt_name = spec.choices.get(dim.name, "?")
        opt = spec.get_option(dim.name)
        marker = " *" if "standard" not in opt.tags else ""
        lines.append(f"  {dim.name:25s} = {opt_name:30s}{marker}")
        lines.append(f"  {'':25s}   {opt.description}")

    lines.append("")
    lines.append(f"  Tags: {', '.join(sorted(spec.all_tags()))}")
    return "\n".join(lines)


