# ── Dimension Definitions ──────────────────────────────────────────────

@dataclass(frozen=True)
class Option:
    """A single option within a dimension."""
    name: str
    description: str
    # Tags for constraint checking (e.g., "linear_complexity", "needs_sorting")
    tags: Tuple[str, ...] = ()
    # Incompatible tags from OTHER dimensions
    incompatible_with: Tuple[str, ...] = ()


@dataclass(frozen=True)
class Dimension:
    """A single axis of variation in the morphological box."""
    name: str
    description: str
    options: Tuple[Option, ...]

    def random_option(self, rng: random.Random) -> Option:
        return rng.choice(self.options)


